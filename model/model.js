export function modelPageName(pgName) {

     
    if(pgName == "homeContent") {
        $("nav .navWrapper .links a").removeClass("blackText").addClass("whiteText");
    }else {
        $("nav .navWrapper .links a").removeClass("whiteText").addClass("blackText");
    }

    if(pgName == "homeContent") {
        $("nav .navWrapper .logo").removeClass("logo").addClass("whiteLogo");
    }
    else {
        $("nav .navWrapper .whiteLogo").removeClass("whiteLogo").addClass("logo");
    }
    
    if(pgName == "homeContent") {
        $("nav .navLine").removeClass("navLine").addClass("whiteLine");
    }
    else {
        $("nav .whiteLine").removeClass("whiteLine").addClass("navLine");
    }


  
    
    

    
 
    

   

    try {
        eval(pgName);
        $("#app").html(eval(pgName));
    }
    catch (error) {
        console.log("hello", error);
    }
}